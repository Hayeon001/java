package variable;

public class P78 {

	public static void main(String[] args) {
		// 대입연산자
				int a = 10;
				a = a + 1;
				System.out.println(a);
				
				int b = 10;
				b = --b + 1;  //b는 9가 됨
				System.out.println(b);

				int c = 20;
				c += 1;
				//c = c + 1;
				System.out.println(c);
	
		
	}

}
